/**
 * 수리는 81을 초과하면 81을 뺌 (1로 환원)
 */
function 수리환원(n) {
  if (n <= 0) return 1;
  if (n <= 81) return n;
  return ((n - 1) % 81) + 1;
}

/**
 * 오격 계산 (한국식 표준)
 *  원격(元格): 이름 (성 제외) 합 — 초년운
 *  형격(亨格): 성 + 이름 첫자 — 청년운
 *  이격(利格): 성 + 이름 마지막자 — 중년운
 *  정격(貞格) = 총격(總格): 전체 합 — 만년·총운
 *  외격(外格) = 정격 - 형격 + 1 (또는 첫자+끝자) — 사회운
 *
 * @param {number[]} surnameStrokes - 성씨 한자 획수 배열 (1~2자)
 * @param {number[]} nameStrokes - 이름 한자 획수 배열 (1~3자)
 */
function calculateOgyeok(surnameStrokes, nameStrokes) {
  const s = surnameStrokes.reduce((a, b) => a + b, 0);
  const n = nameStrokes.reduce((a, b) => a + b, 0);
  const 원격 = nameStrokes.length >= 2
  ? n // 이름 전체 합
  : nameStrokes[0] + 1; // 외자 이름 보정
  const 형격 = s + nameStrokes[0];
  const 이격 = nameStrokes.length >= 2
  ? s + nameStrokes[nameStrokes.length - 1]
  : s + 1; // 외자 이름 보정
  const 정격 = s + n; // 총격
  const 외격 = 정격 - 형격 + 1;
  return {
  원격: 수리환원(원격),
  형격: 수리환원(형격),
  이격: 수리환원(이격),
  정격: 수리환원(정격),
  외격: 수리환원(외격),
  };
}

/**
 * 한글 이름의 음오행 분석
 *  - 각 글자 초성의 오행을 추출
 *  - 인접한 음 간의 상생/상극 관계 평가
 *  - 상생(생): 목→화→토→금→수→목, 상극(극): 목극토, 토극수, 수극화, 화극금, 금극목
 */
function analyzeEumOhaeng(hangulName) {
  const chars = Array.from(hangulName).filter(c => c.charCodeAt(0) >= 0xAC00 && c.charCodeAt(0) <= 0xD7A3);
  const ohaengs = chars.map(c => {
  const cs = getChoseong(c);
  return cs !== null ? 한글_초성_오행[cs] ?? -1 : -1;
  });
  // 인접 관계 평가
  const relations = [];
  for (let i = 0; i < ohaengs.length - 1; i++) {
  const a = ohaengs[i], b = ohaengs[i + 1];
  if (a < 0 || b < 0) { relations.push('?'); continue; }
  if (a === b) relations.push('비화'); // 같은 오행
  else if ((a + 1) % 5 === b) relations.push('상생'); // a가 b를 생함
  else if ((b + 1) % 5 === a) relations.push('생받음'); // b가 a를 생함
  else if ((a + 2) % 5 === b) relations.push('상극'); // a가 b를 극함
  else if ((b + 2) % 5 === a) relations.push('극받음'); // b가 a를 극함
  else relations.push('-');
  }
  // 전체 평가: 상생이 많을수록 좋음, 상극은 흉
  const goodCount = relations.filter(r => r === '상생' || r === '생받음' || r === '비화').length;
  const badCount  = relations.filter(r => r === '상극' || r === '극받음').length;
  let 평가;
  if (goodCount > badCount) 평가 = '길 (상생 우세)';
  else if (badCount > goodCount) 평가 = '흉 (상극 우세)';
  else 평가 = '평 (혼재)';
  return { 글자: chars, 초성오행: ohaengs, 관계: relations, 평가 };
}

/**
 * 음양 분석: 한자 획수의 홀짝 분포
 *  홀수=양(陽), 짝수=음(陰)
 *  양과 음이 조화(섞임)되면 길, 모두 양 또는 모두 음이면 흉
 */
function analyzeEumYang(strokes) {
  const yinYangs = strokes.map(n => n % 2 === 1 ? '양' : '음');
  const yangCount = yinYangs.filter(y => y === '양').length;
  const yinCount = yinYangs.length - yangCount;
  let 평가;
  if (yangCount === 0 || yinCount === 0) 평가 = '흉 (편중)';
  else if (Math.abs(yangCount - yinCount) <= 1) 평가 = '길 (조화)';
  else 평가 = '평';
  return { 획수음양: yinYangs, 양: yangCount, 음: yinCount, 평가 };
}

