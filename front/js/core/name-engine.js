/**
 * 한글 전체이름 → 성 + 이름 분리
 *  1자: 성만 (이름 비움)
 *  2자: 성 1 + 이름 1
 *  3자: 성 1 + 이름 2 (예: 박형민)
 *  4자: 앞 2자가 복성이면 복성 + 이름 2 (예: 남궁민수), 아니면 성 1 + 이름 3
 *  5자: 복성 + 이름 3
 */
function splitName(fullName) {
  const chars = Array.from((fullName || '').trim());
  if (chars.length === 0) return { surname: '', givenName: '' };
  if (chars.length === 1) return { surname: chars[0], givenName: '' };
  if (chars.length >= 3) {
  const first2 = chars[0] + chars[1];
  if (복성_목록.includes(first2)) {
  return { surname: first2, givenName: chars.slice(2).join('') };
  }
  }
  return { surname: chars[0], givenName: chars.slice(1).join('') };
}
window.splitName = splitName;

