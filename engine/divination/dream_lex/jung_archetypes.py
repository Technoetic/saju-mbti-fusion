_ARCHETYPE_DICTS = {
    "shadow": SHADOW_SYMBOLS,
    "anima": ANIMA_SYMBOLS,
    "animus": ANIMUS_SYMBOLS,
    "self": SELF_SYMBOLS,
    "persona": PERSONA_SYMBOLS,
}


def lookup_archetypes(text: str, gender: str | None = None, limit: int = 12) -> dict[str, Any]:
    """원형 매핑 + 임계 공간 별도 집계.

    gender: 'M' 또는 'F'면 아니마·아니무스 중 부적합한 쪽 가중치 절반.
    """
    t = text or ""
    archetype_hits: dict[str, list[dict[str, str]]] = {k: [] for k in _ARCHETYPE_DICTS}

    for archetype, dictionary in _ARCHETYPE_DICTS.items():
        for kw, meaning in dictionary.items():
            if kw in t:
                archetype_hits[archetype].append({
                    "keyword": kw,
                    "archetype": archetype,
                    "meaning": meaning,
                })

    liminal_hits: list[dict[str, str]] = []
    for kw, meaning in LIMINAL_SYMBOLS.items():
        if kw in t:
            liminal_hits.append({"keyword": kw, "meaning": meaning})

    # 성별 가중치
    weights = {k: 1.0 for k in _ARCHETYPE_DICTS}
    if gender == "M":
        weights["animus"] = 0.5
    elif gender == "F":
        weights["anima"] = 0.5

    weighted_counts = {k: len(v) * weights[k] for k, v in archetype_hits.items()}
    dominant = max(weighted_counts, key=lambda k: weighted_counts[k]) if any(weighted_counts.values()) else None

    # 결과 평탄화 (상위 limit개)
    flat: list[dict[str, str]] = []
    for archetype, hits in archetype_hits.items():
        flat.extend(hits)
    flat = flat[:limit]

    return {
        "archetype_hits": flat,
        "by_archetype": archetype_hits,
        "dominant_archetype": dominant,
        "dominant_label": JUNG_ARCHETYPES.get(dominant) if dominant else None,
        "liminal_spaces": liminal_hits,
        "is_transformational": len(liminal_hits) > 0,
    }


