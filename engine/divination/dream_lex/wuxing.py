def map_to_wuxing(text: str, limit: int = 12) -> dict[str, Any]:
    """꿈 텍스트에서 등장한 키워드들을 오행으로 매핑·집계."""
    t = text or ""
    matched: list[dict[str, Any]] = []
    counts = {"木": 0, "火": 0, "土": 0, "金": 0, "水": 0}

    for kw, elements in DREAM_TO_WUXING.items():
        if kw in t:
            matched.append({"keyword": kw, "elements": elements})
            for e in elements:
                counts[e] += 1
            if len(matched) >= limit:
                break

    total = sum(counts.values()) or 1
    distribution = {k: round(v / total * 100, 1) for k, v in counts.items()}
    dominant = max(counts, key=lambda k: counts[k]) if total > 1 else None

    return {
        "matched": matched,
        "counts": counts,
        "distribution_pct": distribution,
        "dominant_element": dominant,
        "dominant_label": WUXING_LABELS.get(dominant) if dominant else None,
    }


def saju_interaction(dream_elements: dict[str, int], yongsin: str | None) -> dict[str, Any]:
    """꿈의 오행 분포 vs 사주 용신(用神)의 상호작용 — 길흉 가중치."""
    if not yongsin or yongsin not in WUXING_LABELS:
        return {"verdict": "중립", "note": "용신 정보 없음 — 일반 해석 적용"}

    dominant = max(dream_elements, key=lambda k: dream_elements[k]) if any(dream_elements.values()) else None
    if not dominant:
        return {"verdict": "중립", "note": "오행 매칭 없음"}

    # 상생: 木→火→土→金→水→木
    generates = {"木": "火", "火": "土", "土": "金", "金": "水", "水": "木"}
    # 상극: 木→土, 土→水, 水→火, 火→金, 金→木
    overcomes = {"木": "土", "土": "水", "水": "火", "火": "金", "金": "木"}

    if dominant == yongsin:
        return {"verdict": "길", "note": f"꿈의 지배 오행({dominant})이 용신과 동일 — 강한 응원"}
    if generates.get(dominant) == yongsin:
        return {"verdict": "길", "note": f"{dominant}이 용신 {yongsin}을 생함 — 도움"}
    if generates.get(yongsin) == dominant:
        return {"verdict": "중립", "note": f"용신이 {dominant}을 생함 — 에너지 소모"}
    if overcomes.get(dominant) == yongsin:
        return {"verdict": "흉", "note": f"{dominant}이 용신 {yongsin}을 극함 — 주의"}
    if overcomes.get(yongsin) == dominant:
        return {"verdict": "중립", "note": f"용신이 {dominant}을 제어 — 안정"}

    return {"verdict": "중립", "note": "특이 상호작용 없음"}


