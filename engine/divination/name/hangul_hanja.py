# 한국 복성 목록
DOUBLE_SURNAMES: tuple[str, ...] = (
    "남궁", "황보", "제갈", "사공", "선우", "서문", "독고", "동방",
    "강전", "사마", "부여",
)


# ─────────────────────────── Public API ───────────────────────────

def get_candidates(syllable: str) -> list[str]:
    """단일 한글 음절의 한자 후보 리스트.

    두음법칙 자동 적용 — '유' 입력 시 '류' 후보도 포함.
    중복은 자동 제거.

    Args:
        syllable: 단일 한글 음절 (예: '성', '유').

    Returns:
        한자 후보 리스트. 없으면 빈 리스트.
    """
    if not syllable:
        return []

    # 두음법칙 확장
    from engine.divination.name.dueum import expand_search_candidates
    variants = expand_search_candidates(syllable)

    out: list[str] = []
    seen: set[str] = set()
    for v in variants:
        for h in HANGUL_HANJA_MAP.get(v, []):
            if h not in seen:
                seen.add(h)
                out.append(h)
    return out


def total_syllables() -> int:
    return len(HANGUL_HANJA_MAP)


def total_hanja() -> int:
    """수록된 한자 총 개수 (중복 제거)."""
    s: set[str] = set()
    for chars in HANGUL_HANJA_MAP.values():
        s.update(chars)
    return len(s)


def is_double_surname(name: str) -> bool:
    """한글 이름의 앞 2자가 복성인지."""
    if not name or len(name) < 2:
        return False
    return name[:2] in DOUBLE_SURNAMES
