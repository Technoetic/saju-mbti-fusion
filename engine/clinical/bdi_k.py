def score_bdi_k(responses: list[int]) -> dict[str, Any]:
    """BDI 한국판 채점.

    Returns:
        결과 dict (자살 사고 항목 9 ≥ 1점 시 suicide_alert=True)
    """
    if len(responses) != 21:
        raise ValueError(f"BDI는 21개 응답 필요 (받음: {len(responses)})")
    for i, r in enumerate(responses):
        if r not in (0, 1, 2, 3):
            raise ValueError(f"응답은 0~3 정수여야 함 (item {i + 1}: {r})")

    total = sum(responses)
    suicide_score = responses[8]  # 9번 (0-idx 8)
    suicide_alert = suicide_score >= 1

    if total <= 9:
        severity = "정상 범위"
    elif total <= 15:
        severity = "경도 우울 의심"
    elif total <= 23:
        severity = "중등도 우울 의심"
    else:
        severity = "중증 우울 의심"

    exceeded = total >= BDI_K_CUTOFF

    return {
        "total_score": total,
        "cutoff": BDI_K_CUTOFF,
        "exceeded_cutoff": exceeded,
        "severity": severity,
        "suicide_item_score": suicide_score,
        "suicide_alert": suicide_alert,
        "interpretive_note": (
            "자살 사고 항목이 0점 초과 — 즉시 전문가 상담을 권합니다."
            if suicide_alert
            else "총점 cutoff 초과 — 정신건강 전문가 상담을 권합니다."
            if exceeded
            else "현 시점 우울 의심 수준 미만. 자가보고는 참고 자료로만 사용."
        ),
        "referral_recommended": exceeded or suicide_alert,
        "instrument": "BDI 한국판 (이영호·송종용 1991)",
    }


